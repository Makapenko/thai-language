// Карта совместимости: thai verb → array of compatible noun thai
// Определяет, какие существительные могут быть объектами для каждого глагола
export const verbObjectCompatibility: Record<string, string[]> = {
  'เปิด': ['ประตู', 'หน้าต่าง', 'หนังสือ', 'กล่อง'],      // открыть
  'ปิด': ['ประตู', 'หน้าต่าง', 'หนังสือ', 'กล่อง'],        // закрыть
  'เห็น': ['คน', 'สุนัข', 'บ้าน', 'รถ', 'ประตู', 'หน้าต่าง'], // видеть
  'ดู': ['ทีวี', 'หนัง', 'คน', 'สุนัข', 'บ้าน'],            // смотреть
  'ซื้อ': ['บ้าน', 'รถ', 'หนังสือ', 'อาหาร', 'เสื้อ'],      // покупать
  'ทำ': ['งาน', 'อาหาร', 'หนังสือ', 'บ้าน'],                // делать
  'เปลี่ยน': ['เสื้อ', 'บ้าน', 'รถ', 'หนังสือ'],            // менять
  'สร้าง': ['บ้าน', 'รถ'],                                  // строить
  'หา': ['งาน', 'เงิน', 'อาหาร', 'คน'],                     // искать
  'ตัด': ['เสื้อ', 'ผม', 'กระดาษ'],                         // резать/стричь
};

// Функция проверки совместимости глагол-существительное
export function isVerbObjectCompatible(verbThai: string, nounThai: string): boolean {
  const compatibleNouns = verbObjectCompatibility[verbThai];
  // Если нет ограничений для этого глагола — разрешаем все комбинации
  if (!compatibleNouns) return true;
  return compatibleNouns.includes(nounThai);
}
